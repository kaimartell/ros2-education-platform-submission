const DOCK_ACTION_CLIENT_SOURCE = [
  "import rclpy",
  "from rclpy.action import ActionClient",
  "from rclpy.node import Node",
  "from irobot_create_msgs.action import Dock",
  "",
  "class DockActionClient(Node):",
  "    def __init__(self):",
  "        super().__init__(\"dock_action_client\")",
  "        self._client = ActionClient(self, Dock, \"dock\")",
  "",
  "    def send_goal(self):",
  "        goal_msg = Dock.Goal()",
  "        self._client.wait_for_server()",
  "        self._send_goal_future = self._client.send_goal_async(",
  "            goal_msg, feedback_callback=self.feedback_callback",
  "        )",
  "        self._send_goal_future.add_done_callback(self.goal_response_callback)",
  "",
  "    def goal_response_callback(self, future):",
  "        goal_handle = future.result()",
  "        self._result_future = goal_handle.get_result_async()",
  "        self._result_future.add_done_callback(self.get_result_callback)",
  "",
  "    def feedback_callback(self, feedback_msg):",
  "        feedback = feedback_msg.feedback",
  "        self.get_logger().info(f\"sees_dock={feedback.sees_dock}\")",
  "",
  "    def get_result_callback(self, future):",
  "        result = future.result().result",
  "        self.get_logger().info(f\"is_docked={result.is_docked}\")",
  "",
  "def main(args=None):",
  "    rclpy.init(args=args)",
  "    node = DockActionClient()",
  "    node.send_goal()",
  "    rclpy.spin(node)",
  "    rclpy.shutdown()",
].join("\n");

const ROVER_CONTROLLER_SOURCE = [
  "import rclpy",
  "from geometry_msgs.msg import Twist",
  "from rclpy.executors import MultiThreadedExecutor",
  "from rclpy.node import Node",
  "from sensor_msgs.msg import Range",
  "",
  "class FrontSensor(Node):",
  "    def __init__(self):",
  "        super().__init__(\"front_sensor\")",
  "        self.publisher_ = self.create_publisher(Range, \"/front_range\", 10)",
  "        self.current_range = 0.0",
  "        self.timer = self.create_timer(0.7, self.publish_range)",
  "    def publish_range(self):",
  "        msg = Range()",
  "        msg.range = self.current_range",
  "        self.publisher_.publish(msg)",
  "",
  "class RearSensor(Node):",
  "    def __init__(self):",
  "        super().__init__(\"rear_sensor\")",
  "        self.publisher_ = self.create_publisher(Range, \"/rear_range\", 10)",
  "        self.current_range = 0.0",
  "        self.timer = self.create_timer(0.9, self.publish_range)",
  "    def publish_range(self):",
  "        msg = Range()",
  "        msg.range = self.current_range",
  "        self.publisher_.publish(msg)",
  "",
  "class RoverController(Node):",
  "    def __init__(self):",
  "        super().__init__(\"rover_controller\")",
  "        self.cmd_pub = self.create_publisher(Twist, \"/cmd_vel\", 10)",
  "        self.create_subscription(Range, \"/front_range\", self.front_range_callback, 10)",
  "        self.create_subscription(Range, \"/rear_range\", self.rear_range_callback, 10)",
  "        self.front_stop_distance = 0.5",
  "        self.rear_stop_distance = 0.4",
  "",
  "    def front_range_callback(self, msg):",
  "        cmd = Twist()",
  "        cmd.linear.x = -0.2 if msg.range < self.front_stop_distance else 0.3",
  "        self.cmd_pub.publish(cmd)",
  "",
  "    def rear_range_callback(self, msg):",
  "        if msg.range < self.rear_stop_distance:",
  "            cmd = Twist()",
  "            cmd.linear.x = 0.3",
  "            self.cmd_pub.publish(cmd)",
  "",
  "class MotorDriver(Node):",
  "    def __init__(self):",
  "        super().__init__(\"motor_driver\")",
  "        self.create_subscription(Twist, \"/cmd_vel\", self.drive_callback, 10)",
  "",
  "    def drive_callback(self, msg):",
  "        self.get_logger().info(f\"drive {msg.linear.x:.1f} m/s\")",
  "",
  "class Logger(Node):",
  "    def __init__(self):",
  "        super().__init__(\"logger\")",
  "        self.create_subscription(Twist, \"/cmd_vel\", self.log_callback, 10)",
  "",
  "    def log_callback(self, msg):",
  "        self.get_logger().info(f\"cmd_vel {msg.linear.x:.1f}\")",
  "",
  "def main(args=None):",
  "    rclpy.init(args=args)",
  "    executor = MultiThreadedExecutor()",
  "    nodes = [FrontSensor(), RearSensor(), RoverController(), MotorDriver(), Logger()]",
  "    for node in nodes:",
  "        executor.add_node(node)",
  "    executor.spin()",
  "    for node in nodes:",
  "        node.destroy_node()",
  "    rclpy.shutdown()",
].join("\n");

const SYMPHONY_SOURCE = [
  "import rclpy",
  "from rclpy.executors import MultiThreadedExecutor",
  "from rclpy.node import Node",
  "from std_msgs.msg import Float32, String",
  "",
  "class Conductor(Node):",
  "    def __init__(self):",
  "        super().__init__(\"conductor\")",
  "        self.tempo_pub = self.create_publisher(Float32, \"/tempo\", 10)",
  "        self.dynamics_pub = self.create_publisher(String, \"/dynamics\", 10)",
  "        self.tempos = [120.0, 100.0, 140.0, 120.0]",
  "        self.dynamics = [\"forte\", \"piano\", \"crescendo\", \"forte\"]",
  "        self.index = 0",
  "        self.create_timer(3.0, self.give_cue)",
  "",
  "    def give_cue(self):",
  "        i = self.index % len(self.tempos)",
  "        tempo = Float32()",
  "        tempo.data = self.tempos[i]",
  "        self.tempo_pub.publish(tempo)",
  "        dyn = String()",
  "        dyn.data = self.dynamics[i]",
  "        self.dynamics_pub.publish(dyn)",
  "        self.index += 1",
  "",
  "class Violin(Node):",
  "    def __init__(self):",
  "        super().__init__(\"violin\")",
  "        self.pub = self.create_publisher(String, \"/violin\", 10)",
  "        self.create_subscription(Float32, \"/tempo\", self.on_tempo, 10)",
  "        self.create_subscription(String, \"/dynamics\", self.on_dynamics, 10)",
  "        self.notes = [\"G4\", \"A4\", \"B4\", \"D5\", \"E5\"]",
  "        self.index = 0",
  "        self.create_timer(0.5, self.play_note)",
  "",
  "    def on_tempo(self, msg):",
  "        self.get_logger().info(f\"tempo: {msg.data}\")",
  "",
  "    def on_dynamics(self, msg):",
  "        self.get_logger().info(f\"dynamics: {msg.data}\")",
  "",
  "    def play_note(self):",
  "        msg = String()",
  "        msg.data = self.notes[self.index % len(self.notes)]",
  "        self.index += 1",
  "        self.pub.publish(msg)",
  "",
  "class Cello(Node):",
  "    def __init__(self):",
  "        super().__init__(\"cello\")",
  "        self.pub = self.create_publisher(String, \"/cello\", 10)",
  "        self.create_subscription(Float32, \"/tempo\", self.on_tempo, 10)",
  "        self.create_subscription(String, \"/dynamics\", self.on_dynamics, 10)",
  "        self.chords = [\"C3\", \"G3\", \"Am\", \"F3\"]",
  "        self.index = 0",
  "        self.create_timer(0.8, self.play_harmony)",
  "",
  "    def on_tempo(self, msg):",
  "        self.get_logger().info(f\"tempo: {msg.data}\")",
  "",
  "    def on_dynamics(self, msg):",
  "        self.get_logger().info(f\"dynamics: {msg.data}\")",
  "",
  "    def play_harmony(self):",
  "        msg = String()",
  "        msg.data = self.chords[self.index % len(self.chords)]",
  "        self.index += 1",
  "        self.pub.publish(msg)",
  "",
  "class Bass(Node):",
  "    def __init__(self):",
  "        super().__init__(\"bass\")",
  "        self.pub = self.create_publisher(Float32, \"/bass\", 10)",
  "        self.create_subscription(Float32, \"/tempo\", self.on_tempo, 10)",
  "        # bass follows tempo only",
  "        self.pattern = [55.0, 41.2, 49.0, 36.7]",
  "        self.index = 0",
  "        self.create_timer(1.2, self.play_line)",
  "",
  "    def on_tempo(self, msg):",
  "        self.get_logger().info(f\"tempo: {msg.data}\")",
  "",
  "    def play_line(self):",
  "        msg = Float32()",
  "        msg.data = self.pattern[self.index % len(self.pattern)]",
  "        self.index += 1",
  "        self.pub.publish(msg)",
  "",
  "class Audience(Node):",
  "    def __init__(self):",
  "        super().__init__(\"audience\")",
  "        self.create_subscription(String, \"/violin\", self.on_violin, 10)",
  "        self.create_subscription(String, \"/cello\", self.on_cello, 10)",
  "        self.create_subscription(Float32, \"/bass\", self.on_bass, 10)",
  "",
  "    def on_violin(self, msg):",
  "        self.get_logger().info(f\"violin: {msg.data}\")",
  "",
  "    def on_cello(self, msg):",
  "        self.get_logger().info(f\"cello: {msg.data}\")",
  "",
  "    def on_bass(self, msg):",
  "        self.get_logger().info(f\"bass: {msg.data:.1f} Hz\")",
  "",
  "class Spotlight(Node):",
  "    def __init__(self):",
  "        super().__init__(\"spotlight\")",
  "        self.create_subscription(String, \"/violin\", self.on_solo, 10)",
  "",
  "    def on_solo(self, msg):",
  "        self.get_logger().info(f\"spotlight on: {msg.data}\")",
  "",
  "def main(args=None):",
  "    rclpy.init(args=args)",
  "    executor = MultiThreadedExecutor()",
  "    nodes = [Conductor(), Violin(), Cello(), Bass(), Audience(), Spotlight()]",
  "    for n in nodes:",
  "        executor.add_node(n)",
  "    executor.spin()",
  "    for n in nodes:",
  "        n.destroy_node()",
  "    rclpy.shutdown()",
].join("\n");

