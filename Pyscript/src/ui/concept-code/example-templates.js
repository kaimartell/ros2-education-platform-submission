const SIMPLE_PUBLISHER_SOURCE = [
  "import rclpy",
  "from rclpy.node import Node",
  "from std_msgs.msg import String",
  "",
  "class SimplePublisher(Node):",
  "    def __init__(self):",
  "        super().__init__(\"simple_publisher\")",
  "        self.publisher_ = self.create_publisher(String, \"my_publisher\", 10)",
  "        timer_period = 0.5",
  "        self.timer = self.create_timer(timer_period, self.timer_callback)",
  "",
  "    def timer_callback(self):",
  "        msg = String()",
  "        msg.data = \"hello ROS 2\"",
  "        self.publisher_.publish(msg)",
  "        self.get_logger().info(f'Publishing: {msg.data}')",
  "",
  "def main(args=None):",
  "    rclpy.init(args=args)",
  "    node = SimplePublisher()",
  "    rclpy.spin(node)",
  "    node.destroy_node()",
  "    rclpy.shutdown()",
].join("\n");

const DOCK_ACTION_CLIENT_SOURCE = [
  "import rclpy",
  "from rclpy.action import ActionClient",
  "from rclpy.node import Node",
  "from irobot_create_msgs.action import Dock",
  "",
  "class DockActionClient(Node):",
  "    def __init__(self):",
  "        super().__init__(\"dock_action_client\")",
  "        self._client = ActionClient(self, Dock, \"dock\")",
  "",
  "    def send_goal(self):",
  "        goal_msg = Dock.Goal()",
  "        self._client.wait_for_server()",
  "        self._send_goal_future = self._client.send_goal_async(",
  "            goal_msg, feedback_callback=self.feedback_callback",
  "        )",
  "        self._send_goal_future.add_done_callback(self.goal_response_callback)",
  "",
  "    def goal_response_callback(self, future):",
  "        goal_handle = future.result()",
  "        self._result_future = goal_handle.get_result_async()",
  "        self._result_future.add_done_callback(self.get_result_callback)",
  "",
  "    def feedback_callback(self, feedback_msg):",
  "        feedback = feedback_msg.feedback",
  "        self.get_logger().info(f\"sees_dock={feedback.sees_dock}\")",
  "",
  "    def get_result_callback(self, future):",
  "        result = future.result().result",
  "        self.get_logger().info(f\"is_docked={result.is_docked}\")",
  "",
  "def main(args=None):",
  "    rclpy.init(args=args)",
  "    node = DockActionClient()",
  "    node.send_goal()",
  "    rclpy.spin(node)",
  "    rclpy.shutdown()",
].join("\n");

